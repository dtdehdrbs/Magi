package magi.board.controller;

public class BoardReBean {
	private int num;
	private int replyseq;
	private String replytitle;
	private String replycontent;
	private String replywriter;
	private String replytime;
	private int replyviewcount;
	private String replytype;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getReplyseq() {
		return replyseq;
	}
	public void setReplyseq(int replyseq) {
		this.replyseq = replyseq;
	}
	public String getReplytitle() {
		return replytitle;
	}
	public void setReplytitle(String replytitle) {
		this.replytitle = replytitle;
	}
	public String getReplycontent() {
		return replycontent;
	}
	public void setReplycontent(String replycontent) {
		this.replycontent = replycontent;
	}
	public String getReplywriter() {
		return replywriter;
	}
	public void setReplywriter(String replywriter) {
		this.replywriter = replywriter;
	}
	public String getReplytime() {
		return replytime;
	}
	public void setReplytime(String replytime) {
		this.replytime = replytime;
	}
	public int getReplyviewcount() {
		return replyviewcount;
	}
	public void setReplyviewcount(int replyviewcount) {
		this.replyviewcount = replyviewcount;
	}
	public String getReplytype() {
		return replytype;
	}
	public void setReplytype(String replytype) {
		this.replytype = replytype;
	}

}
